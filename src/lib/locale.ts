export type TranslationText = typeof uiText[Locale];

export function localizeObject<T extends object>(base: T, locale: Locale, translations?: { en?: Partial<T>; ru?: Partial<T> }): T {
  if (!translations) return base;
  return { ...base, ...(translations[locale] ?? {}) } as T;
}
