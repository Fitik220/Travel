type GeoJsonGeometry = {
  type?: string;
  coordinates?: unknown;
};

const slugify = (value: string) =>
  value
    .toLowerCase()
    .replace(/’/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

const colorForName = (name: string) => {
  const hash = Array.from(name).reduce((acc, char) => acc + char.charCodeAt(0), 0);
  return `hsl(${hash % 360}, 72%, 60%)`;
};

const averagePoint = (points: [number, number][]) => {
  const total = points.reduce(
    (acc, [lng, lat]) => ({ lat: acc.lat + lat, lng: acc.lng + lng }),
    { lat: 0, lng: 0 },
  );
  return { lat: total.lat / points.length, lng: total.lng / points.length };
};

const extractCoordinates = (geometry: GeoJsonGeometry | null | undefined): [number, number][][] => {
  if (!geometry) return [];
  if (geometry.type === "Polygon") return geometry.coordinates as [number, number][][];
  if (geometry.type === "MultiPolygon") return (geometry.coordinates as [number, number][][][]).flat();
  return [];
};

const computeCentroid = (geometry: GeoJsonGeometry) => {
  const polygons = extractCoordinates(geometry);
  if (!polygons.length) return { lat: 0, lng: 0 };
  const points = polygons.flat();
  return averagePoint(points);
};

